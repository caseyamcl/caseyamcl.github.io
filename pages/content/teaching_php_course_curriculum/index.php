<p>
	The problem, succinctly stated, is: <strong>How do you best organize 14 weeks of
	study to teach PHP to a group of motivated College students?</strong>
</p>

<p>
	I know they must be pretty motivated, because they’re signing up for this
	optional elective despite the fact that it starts at 8 am.
</p>

<p>
	This Fall, I will  be teaching a course in Web Application Development at
	FSU.  The topic will be server-side scripting, and the focus will be PHP.
	Since I’ve taught this course before, developing the curriculum won’t take
	months, but I am nevertheless spending a fair amount of time updating and
	tweaking for the next go-around.
</p>

<p>
	A quick <a href="http://spedr.com/xyd0" title="Searchin Google">Google Search for "Teaching PHP</a>
	brings up only a few relevant links, mostly message board discussions. 
	“Server Side Scripting Curriculum” doesn't return much either.  Likewise, the
	<a href="http://interact.webstandards.org/" title='Web Standards Project'>WaSP
	Server-Side Scripting  Curriculum</a> only gets us halfway to where we need
	to be by the end of the term.
</p>

<p>
	So, I’ve had to come up with a lot of the course unilaterally.
	The learning objectives are as follows:
</p>

<p>
	By the end of the course, students will understand:
</p>

<ul>
	<li>Basic programming skills</li>
	<li>Web application development principles, including <a href="http://en.wikipedia.org/wiki/Model%E2%80%93view%E2%80%93controller" title="What?  You don't know what MVC is?">MVC</a></li>
	<li>How to find and use PHP resources on the Internet</li>
	<li>How to manage tasks, bugs, and revision control mechanisms for web projects</li>
</ul>

<p>
	In addition, students should be able to:
</p>

<ul>
	<li>Program and deploy simple scripts in PHP</li>
	<li>Process web-based input and output in PHP</li>
	<li>Build multi-file PHP applications</li>
	<li>Connect PHP applications to a database</li>
	<li>Design a simple dynamic website using PHP, MySQL, HTML, and CSS</li>	
</ul>

<p>
	After a few years of teaching the course, I've managed to find a way to squeeze
	these topics into the course of the semester:
</p>

<ol>
	<li>The current “State of the Internet”</li>
	<li>Overview of HTTP</li>
	<li>Basic GIT Revision Control</li>
	<li>Web resources - PHP.net, Stackoverflow.com, etc</li>
	<li>Netbeans setup and good practices for personal development workflow</li>
	<li>PHP basics – Syntax, variables, arrays, functions, iterators, etc.</li>
	<li>Form processing and security</li>
	<li>Classes and objects</li>
	<li>MYSQL Database connectivity and integration</li>
	<li>Sessions and authentication</li>
	<li>Front Controller design pattern</li>
	<li>MVC frameworks (we use <a href="http://codeigniter.com" title="CodeIgniter Homepage">CodeIgniter</a>, because of its great documentation)</li>
	<li>Basic management tools: Ticket and bug tracking</li>
	<li>XML and JSON processing</li>
	<li>Web Services - Building a REST client</li>
	<li>Unit Testing - Just a one-day intro; nothing comprehensive</li>
	<li>AJAX - Again, just a basic introduction</li>
</ol>

<p>
	That's <em>a lot</em> of information.  In fact, the students have officially
	given me the reputation as a "hard instructor".  Meh, I'll take it.  
</p>

<p>
	I really would love to teach a second-level course if I ever have time to do 
	so.  Despite the fact that we cover a lot in the first course, there are many
	topics that just don't fit into a single semester (dependency injection, loose
	coupling, comprehensive testing, design principles, development workflow, etc).
</p>	
</p>

